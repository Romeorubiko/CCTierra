    public void actualizarConexion(){
        
        conexion_texto_ip.setText("10.0.1.1");
        conexion_texto_puerto.setText("4040");
        for (String conexionesReciente : conexionesRecientes) {
            conexion_textPanel_ippuerto.setText(
                conexion_textPanel_ippuerto.getText() + 
                conexionesReciente + "\n");
        }
        conexion_textPanel_ippuerto.repaint();
      
    }
    
    
    private void leerConexionesRecientes() throws FileNotFoundException, IOException{
        String cadena;
        FileReader f = new FileReader(CONEXIONESRECIENTES);
        BufferedReader b = new BufferedReader(f);
        while((cadena = b.readLine())!=null) {
            if(cadena.contains(":")){
                conexionesRecientes.add(0, cadena);
            }
            else{
                conexionesRecientes.clear();
                return;
            }
        }
        b.close();
    }
    
    public void guardarConexionesRecientes() throws IOException{
        FileWriter f = new FileWriter(CONEXIONESRECIENTES);
        for (String linea : conexionesRecientes) {
                f.write(linea + "\n");
        }
        f.close();
    }
    
    public void addConexionReciente(String r){
        conexionesRecientes.add(0, r);
        if(conexionesRecientes.size() > 10) conexionesRecientes.remove(10);
    }

    
